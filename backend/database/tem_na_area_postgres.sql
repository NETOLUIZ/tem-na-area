CREATE TABLE IF NOT EXISTS solicitacoes_cadastro (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  protocolo VARCHAR(30) NOT NULL UNIQUE,
  tipo_solicitacao VARCHAR(30) NOT NULL,
  nome_empresa VARCHAR(180) NOT NULL,
  nome_responsavel VARCHAR(150),
  email VARCHAR(190),
  telefone VARCHAR(30),
  whatsapp VARCHAR(30) NOT NULL,
  cpf_cnpj VARCHAR(20),
  categoria_principal VARCHAR(100) NOT NULL,
  descricao_resumida VARCHAR(255),
  cidade VARCHAR(120),
  estado CHAR(2),
  endereco_logradouro VARCHAR(180),
  endereco_numero VARCHAR(20),
  endereco_bairro VARCHAR(120),
  endereco_complemento VARCHAR(120),
  cep VARCHAR(12),
  horario_funcionamento VARCHAR(180),
  logo_url VARCHAR(255),
  capa_url VARCHAR(255),
  observacoes TEXT,
  plano_id BIGINT NOT NULL REFERENCES planos(id) ON DELETE RESTRICT,
  usuario_id BIGINT REFERENCES usuarios(id) ON DELETE SET NULL,
  dono_loja_id BIGINT REFERENCES donos_loja(id) ON DELETE SET NULL,
  status_solicitacao VARCHAR(40) NOT NULL DEFAULT 'PENDENTE',
  status_pagamento VARCHAR(40) NOT NULL DEFAULT 'NAO_APLICAVEL',
  analisado_por_admin_id BIGINT REFERENCES administradores(id) ON DELETE SET NULL,
  analisado_em TIMESTAMPTZ,
  motivo_rejeicao TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS lojas (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uuid UUID NOT NULL UNIQUE,
  dono_loja_id BIGINT NOT NULL REFERENCES donos_loja(id) ON DELETE RESTRICT,
  plano_id BIGINT NOT NULL REFERENCES planos(id) ON DELETE RESTRICT,
  solicitacao_cadastro_id BIGINT REFERENCES solicitacoes_cadastro(id) ON DELETE SET NULL,
  nome VARCHAR(180) NOT NULL,
  slug VARCHAR(180) NOT NULL UNIQUE,
  categoria_principal VARCHAR(100) NOT NULL,
  descricao_curta VARCHAR(255),
  descricao_completa TEXT,
  email_contato VARCHAR(190),
  telefone VARCHAR(30),
  whatsapp VARCHAR(30) NOT NULL,
  website_url VARCHAR(255),
  instagram_url VARCHAR(255),
  facebook_url VARCHAR(255),
  logo_url VARCHAR(255),
  capa_url VARCHAR(255),
  endereco_cep VARCHAR(12),
  endereco_logradouro VARCHAR(180),
  endereco_numero VARCHAR(20),
  endereco_complemento VARCHAR(120),
  endereco_bairro VARCHAR(120),
  endereco_cidade VARCHAR(120),
  endereco_estado CHAR(2),
  latitude NUMERIC(10,7),
  longitude NUMERIC(10,7),
  horario_funcionamento VARCHAR(180),
