CREATE TABLE IF NOT EXISTS product_option_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id BIGINT NOT NULL REFERENCES product_option_groups(id) ON DELETE CASCADE,
  nome VARCHAR(120) NOT NULL,
  descricao VARCHAR(255),
  preco_adicional NUMERIC(10,2) NOT NULL DEFAULT 0,
  ordem_exibicao INTEGER NOT NULL DEFAULT 0,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS product_group_links (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL REFERENCES produtos(id) ON DELETE CASCADE,
  group_id BIGINT NOT NULL REFERENCES product_option_groups(id) ON DELETE CASCADE,
  ordem_exibicao INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (product_id, group_id)
);

