CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS planos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo VARCHAR(50) NOT NULL UNIQUE,
  nome VARCHAR(120) NOT NULL,
  descricao TEXT,
  tipo_exibicao VARCHAR(30) NOT NULL,
  preco_mensal NUMERIC(10,2) NOT NULL DEFAULT 0,
  permite_cardapio BOOLEAN NOT NULL DEFAULT FALSE,
  permite_produtos BOOLEAN NOT NULL DEFAULT FALSE,
  permite_pedidos BOOLEAN NOT NULL DEFAULT FALSE,
  permite_relatorios BOOLEAN NOT NULL DEFAULT FALSE,
  limite_produtos INTEGER,
  limite_banners INTEGER,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS usuarios (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  uuid UUID NOT NULL UNIQUE,
  nome VARCHAR(150) NOT NULL,
  email VARCHAR(190) NOT NULL UNIQUE,
  telefone VARCHAR(30) UNIQUE,
  whatsapp VARCHAR(30),
  senha_hash VARCHAR(255) NOT NULL,
  tipo_usuario VARCHAR(30) NOT NULL,
  status VARCHAR(30) NOT NULL DEFAULT 'PENDENTE',
  ultimo_login_em TIMESTAMPTZ,
  email_verificado_em TIMESTAMPTZ,
  telefone_verificado_em TIMESTAMPTZ,
  avatar_url VARCHAR(255),
  observacoes_internas TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  deleted_at TIMESTAMPTZ
);

CREATE TABLE IF NOT EXISTS clientes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id BIGINT NOT NULL UNIQUE REFERENCES usuarios(id) ON DELETE RESTRICT,
  cpf_cnpj VARCHAR(20),
  data_nascimento DATE,
  genero VARCHAR(30),
  endereco_principal_cep VARCHAR(12),
  endereco_principal_logradouro VARCHAR(180),
  endereco_principal_numero VARCHAR(20),
  endereco_principal_complemento VARCHAR(120),
  endereco_principal_bairro VARCHAR(120),
  endereco_principal_cidade VARCHAR(120),
  endereco_principal_estado CHAR(2),
  referencia_endereco VARCHAR(180),
  aceita_marketing BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS administradores (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id BIGINT NOT NULL UNIQUE REFERENCES usuarios(id) ON DELETE RESTRICT,
  nivel_acesso VARCHAR(30) NOT NULL DEFAULT 'OPERADOR',
  departamento VARCHAR(100),
  ultimo_acesso_painel_em TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS donos_loja (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id BIGINT NOT NULL UNIQUE REFERENCES usuarios(id) ON DELETE RESTRICT,
  nome_fantasia VARCHAR(180),
  razao_social VARCHAR(180),
  cpf_cnpj VARCHAR(20) UNIQUE,
  inscricao_estadual VARCHAR(30),
  data_adesao TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
