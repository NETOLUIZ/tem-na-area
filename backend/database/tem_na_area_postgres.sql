CREATE TABLE IF NOT EXISTS categorias (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  loja_id BIGINT NOT NULL REFERENCES lojas(id) ON DELETE CASCADE,
  nome VARCHAR(120) NOT NULL,
  slug VARCHAR(150) NOT NULL,
  descricao VARCHAR(255),
  tipo_categoria VARCHAR(20) NOT NULL DEFAULT 'AMBOS',
  ordem_exibicao INTEGER NOT NULL DEFAULT 0,
  ativo BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (loja_id, slug)
);

CREATE TABLE IF NOT EXISTS cardapios (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  loja_id BIGINT NOT NULL REFERENCES lojas(id) ON DELETE CASCADE,
  categoria_id BIGINT REFERENCES categorias(id) ON DELETE SET NULL,
  nome VARCHAR(120) NOT NULL,
  descricao VARCHAR(255),
  status_cardapio VARCHAR(20) NOT NULL DEFAULT 'ATIVO',
  ordem_exibicao INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS produtos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  loja_id BIGINT NOT NULL REFERENCES lojas(id) ON DELETE CASCADE,
  categoria_id BIGINT REFERENCES categorias(id) ON DELETE SET NULL,
  cardapio_id BIGINT REFERENCES cardapios(id) ON DELETE SET NULL,
  sku VARCHAR(60),
  nome VARCHAR(150) NOT NULL,
  slug VARCHAR(180) NOT NULL,
  descricao TEXT,
  descricao_curta VARCHAR(255),
  imagem_url VARCHAR(255),
  preco NUMERIC(10,2) NOT NULL,
  preco_promocional NUMERIC(10,2),
  custo NUMERIC(10,2),
  estoque_atual INTEGER NOT NULL DEFAULT 0,
  estoque_minimo INTEGER NOT NULL DEFAULT 0,
  controla_estoque BOOLEAN NOT NULL DEFAULT TRUE,
  permite_venda_sem_estoque BOOLEAN NOT NULL DEFAULT FALSE,
  destaque_home BOOLEAN NOT NULL DEFAULT FALSE,
  peso_gramas INTEGER,
  status_produto VARCHAR(20) NOT NULL DEFAULT 'ATIVO',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  deleted_at TIMESTAMPTZ,
  UNIQUE (loja_id, slug),
  UNIQUE (loja_id, sku)
);

CREATE TABLE IF NOT EXISTS pedidos (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo VARCHAR(30) NOT NULL UNIQUE,
  carrinho_id BIGINT,
  loja_id BIGINT NOT NULL REFERENCES lojas(id) ON DELETE CASCADE,
  cliente_id BIGINT NOT NULL REFERENCES clientes(id) ON DELETE RESTRICT,
  numero_pedido_loja INTEGER,
  status_pedido VARCHAR(30) NOT NULL DEFAULT 'NOVO',
  status_pagamento VARCHAR(30) NOT NULL DEFAULT 'PENDENTE',
  tipo_entrega VARCHAR(20) NOT NULL DEFAULT 'ENTREGA',
  canal_venda VARCHAR(20) NOT NULL DEFAULT 'SITE',
  nome_cliente VARCHAR(150) NOT NULL,
  telefone_cliente VARCHAR(30) NOT NULL,
  email_cliente VARCHAR(190),
  endereco_entrega_cep VARCHAR(12),
  endereco_entrega_logradouro VARCHAR(180),
  endereco_entrega_numero VARCHAR(20),
  endereco_entrega_complemento VARCHAR(120),
  endereco_entrega_bairro VARCHAR(120),
  endereco_entrega_cidade VARCHAR(120),
  endereco_entrega_estado CHAR(2),
  referencia_entrega VARCHAR(180),
  observacoes_cliente TEXT,
  subtotal NUMERIC(10,2) NOT NULL DEFAULT 0,
  desconto NUMERIC(10,2) NOT NULL DEFAULT 0,
  taxa_entrega NUMERIC(10,2) NOT NULL DEFAULT 0,
  total NUMERIC(10,2) NOT NULL DEFAULT 0,
  data_confirmacao TIMESTAMPTZ,
  data_saida_entrega TIMESTAMPTZ,
  data_conclusao TIMESTAMPTZ,
  cancelado_em TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS itens_pedido (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pedido_id BIGINT NOT NULL REFERENCES pedidos(id) ON DELETE CASCADE,
  produto_id BIGINT REFERENCES produtos(id) ON DELETE RESTRICT,
  produto_nome VARCHAR(150) NOT NULL,
  sku_produto VARCHAR(60),
  quantidade INTEGER NOT NULL DEFAULT 1,
  preco_unitario NUMERIC(10,2) NOT NULL,
  desconto_unitario NUMERIC(10,2) NOT NULL DEFAULT 0,
  subtotal NUMERIC(10,2) NOT NULL,
  observacoes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS historico_status_pedido (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pedido_id BIGINT NOT NULL REFERENCES pedidos(id) ON DELETE CASCADE,
