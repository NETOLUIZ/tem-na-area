CREATE TABLE IF NOT EXISTS configuracoes_loja (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  loja_id BIGINT NOT NULL UNIQUE REFERENCES lojas(id) ON DELETE CASCADE,
  cor_primaria VARCHAR(20),
  cor_secundaria VARCHAR(20),
  taxa_entrega_padrao NUMERIC(10,2) NOT NULL DEFAULT 0,
  pedido_minimo NUMERIC(10,2) NOT NULL DEFAULT 0,
  tempo_medio_preparo_minutos INTEGER,
  tempo_medio_entrega_minutos INTEGER,
  aceita_retirada BOOLEAN NOT NULL DEFAULT TRUE,
  aceita_entrega BOOLEAN NOT NULL DEFAULT TRUE,
  exibir_produtos_esgotados BOOLEAN NOT NULL DEFAULT FALSE,
  exibir_whatsapp BOOLEAN NOT NULL DEFAULT TRUE,
  mensagem_boas_vindas TEXT,
  politica_troca TEXT,
  politica_entrega TEXT,
  seo_title VARCHAR(255),
  seo_description TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
